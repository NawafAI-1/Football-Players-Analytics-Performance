class PitchROI:
    def __init__(self, update_interval: int = 30):
        self.interval   = update_interval
        self.roi        = None
        self._last_fn   = -999

    def update(self, frame: np.ndarray, fn: int) -> Tuple[int,int,int,int]:
        if fn - self._last_fn < self.interval and self.roi is not None:
            return self.roi
        H, W = frame.shape[:2]
        hsv  = cv2.cvtColor(frame, cv2.COLOR_BGR2HSV)
        lower = np.array([28,  40,  40])
        upper = np.array([88, 255, 210])
        mask  = cv2.inRange(hsv, lower, upper)
        k     = cv2.getStructuringElement(cv2.MORPH_RECT, (20, 20))
        mask  = cv2.morphologyEx(mask, cv2.MORPH_OPEN,  k)
        mask  = cv2.morphologyEx(mask, cv2.MORPH_CLOSE, k)
        cnts, _ = cv2.findContours(mask, cv2.RETR_EXTERNAL, cv2.CHAIN_APPROX_SIMPLE)
        if cnts:
            largest = max(cnts, key=cv2.contourArea)
            if cv2.contourArea(largest) > W * H * 0.08:
                rx, ry, rw, rh = cv2.boundingRect(largest)
                shrink_x = int(rw * 0.03)
                shrink_y = int(rh * 0.03)
                rx1 = max(0, rx + shrink_x)
                ry1 = max(0, ry + shrink_y)
                rx2 = min(W, rx + rw - shrink_x)
                ry2 = min(H, ry + rh - shrink_y)
                rx1 = max(rx1, int(W * PITCH_LEFT))
                ry1 = max(ry1, int(H * PITCH_TOP))
                rx2 = min(rx2, int(W * PITCH_RIGHT))
                ry2 = min(ry2, int(H * PITCH_BOT))
                self.roi      = (rx1, ry1, rx2, ry2)
                self._last_fn = fn
                return self.roi
        self.roi = (int(W*PITCH_LEFT), int(H*PITCH_TOP),
                    int(W*PITCH_RIGHT), int(H*PITCH_BOT))
        self._last_fn = fn
        return self.roi

    def inside(self, cx, cy, fn, frame):
        roi = self.update(frame, fn)
        x1, y1, x2, y2 = roi
        return x1 <= cx <= x2 and y1 <= cy <= y2


