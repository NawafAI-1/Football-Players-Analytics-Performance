def make_heatmap(players,team,W,H,path):
    canvas=np.zeros((H,W,3),np.uint8)
    cv2.rectangle(canvas,(0,0),(W,H),(30,100,30),-1)
    mx,my=40,30
    cv2.rectangle(canvas,(mx,my),(W-mx,H-my),(255,255,255),2)
    cv2.line(canvas,(W//2,my),(W//2,H-my),(255,255,255),2)
    cv2.circle(canvas,(W//2,H//2),int(H*0.13),(255,255,255),2)
    heat=np.zeros((H,W),np.float32)
    for p in players.values():
        if p.team!=team: continue
        for (px2,py2) in p.stats.positions:
            y1,y2_=max(0,py2-70),min(H,py2+70)
            x1,x2_=max(0,px2-70),min(W,px2+70)
            Y,X=np.ogrid[y1:y2_,x1:x2_]
            heat[y1:y2_,x1:x2_]+=np.maximum(0,1-np.sqrt((X-px2)**2+(Y-py2)**2)/70)**2
    if heat.max()>0: heat=np.clip(heat/heat.max(),0,1)
    cm=cv2.applyColorMap((heat*255).astype(np.uint8),cv2.COLORMAP_JET)
    canvas=(canvas*(1-heat[:,:,np.newaxis]*0.7)+cm*heat[:,:,np.newaxis]*0.7).astype(np.uint8)
    cv2.putText(canvas,f"{TEAM_NAMES[team]} Heatmap",(mx+10,my+24),cv2.FONT_HERSHEY_DUPLEX,0.7,(255,255,255),1)
    cv2.imwrite(path,canvas); print(f"[OK]  Heatmap → {path}")


def make_passnet(players,events,team,W,H,path):
    canvas=np.zeros((H,W,3),np.uint8)
    cv2.rectangle(canvas,(0,0),(W,H),(25,80,25),-1)
    mx,my=40,30
    cv2.rectangle(canvas,(mx,my),(W-mx,H-my),(200,200,200),2)
    cv2.line(canvas,(W//2,my),(W//2,H-my),(200,200,200),2)
    pairs:Dict[tuple,int]={}
    for ev in events:
        if ev.team!=team: continue
        k=(min(ev.from_pid,ev.to_pid),max(ev.from_pid,ev.to_pid))
        pairs[k]=pairs.get(k,0)+1
    avg_pos={}
    for pid,p in players.items():
        if p.team==team and p.stats.positions:
            xs=[x for x,y in p.stats.positions]; ys=[y for x,y in p.stats.positions]
            avg_pos[pid]=(int(sum(xs)/len(xs)),int(sum(ys)/len(ys)))
    maxc=max(pairs.values()) if pairs else 1; col=TC[team]
    for (a,b),cnt in pairs.items():
        if a not in avg_pos or b not in avg_pos: continue
        cv2.line(canvas,avg_pos[a],avg_pos[b],col,max(1,int(cnt/maxc*6)),cv2.LINE_AA)
        mx2=(avg_pos[a][0]+avg_pos[b][0])//2; my2=(avg_pos[a][1]+avg_pos[b][1])//2
        cv2.putText(canvas,str(cnt),(mx2,my2),cv2.FONT_HERSHEY_SIMPLEX,0.32,WHITE,1)
    for pid,pos in avg_pos.items():
        p=players[pid]
        cv2.circle(canvas,pos,16,col,-1,cv2.LINE_AA); cv2.circle(canvas,pos,16,WHITE,1,cv2.LINE_AA)
        s=str(pid); (tw,th2),_=cv2.getTextSize(s,cv2.FONT_HERSHEY_DUPLEX,0.45,1)
        cv2.putText(canvas,s,(pos[0]-tw//2,pos[1]+th2//2),cv2.FONT_HERSHEY_DUPLEX,0.45,BLACK,1)
        cv2.putText(canvas,f"{p.stats.passes_made}p",(pos[0]-8,pos[1]+28),cv2.FONT_HERSHEY_SIMPLEX,0.28,(220,220,220),1)
    cv2.putText(canvas,f"{TEAM_NAMES[team]} Pass Network",(mx+10,my+24),cv2.FONT_HERSHEY_DUPLEX,0.7,WHITE,1)
    cv2.imwrite(path,canvas); print(f"[OK]  Pass network → {path}")


