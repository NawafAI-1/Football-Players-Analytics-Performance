def learn_colors(video_path,clf,model,n_frames=60):
    cap=cv2.VideoCapture(video_path)
    tot=int(cap.get(cv2.CAP_PROP_FRAME_COUNT))
    W=int(cap.get(cv2.CAP_PROP_FRAME_WIDTH))
    H=int(cap.get(cv2.CAP_PROP_FRAME_HEIGHT))
    idxs=np.linspace(0,min(tot-1,1500),n_frames,dtype=int)
    pitch_roi=PitchROI(); hsv_samples=[]; bgr_samples=[]
    for idx in idxs:
        cap.set(cv2.CAP_PROP_POS_FRAMES,int(idx))
        ret,frame=cap.read()
        if not ret: continue
        roi=pitch_roi.update(frame,int(idx)); rx1,ry1,rx2,ry2=roi
        res=model(frame,classes=[PERSON_CLS],conf=0.40,verbose=False)[0]
        for box in res.boxes:
            xy=box.xyxy[0].cpu().numpy(); x1,y1,x2,y2=map(int,xy); cx,cy=(x1+x2)/2,(y1+y2)/2
            if not (rx1<=cx<=rx2 and ry1<=cy<=ry2): continue
            if y2>ry2: continue
            bh,bw=y2-y1,x2-x1
            if bh<H*MIN_BOX_H_FRAC or bw>H*MAX_BOX_W_FRAC: continue
            if bh*bw<MIN_BOX_AREA or bw>bh*1.1: continue
            if float(box.conf[0])<0.40: continue
            hsv=clf.jersey_hsv(frame,xy); bgr=clf.jersey_color(frame,xy)
            if hsv is not None: hsv_samples.append(hsv); bgr_samples.append(bgr)
    cap.release()
    print(f"  Collected {len(hsv_samples)} jersey samples from {n_frames} frames")
    clf.fit(hsv_samples,bgr_samples)


