@dataclass
class PlayerStats:
    pid:int; team:int
    total_dist:float=0.0; top_speed:float=0.0; avg_speed:float=0.0
    sprint_cnt:int=0; sprint_dist:float=0.0; hi_dist:float=0.0
    passes_made:int=0; passes_recv:int=0; poss_frames:int=0
    zone_counts:List[int]=field(default_factory=lambda:[0]*9)
    speed_hist:List[float]=field(default_factory=list)
    positions:List[Tuple]=field(default_factory=list)

    def tick(self, spd, dist):
        self.total_dist+=dist; self.speed_hist.append(round(spd,2))
        if spd>self.top_speed: self.top_speed=spd
        self.avg_speed=sum(self.speed_hist)/len(self.speed_hist)
        if spd>=SPRINT_KMH:
            self.sprint_dist+=dist
            if len(self.speed_hist)<2 or self.speed_hist[-2]<SPRINT_KMH:
                self.sprint_cnt+=1
        if spd>=HI_KMH: self.hi_dist+=dist

    def zone_idx(self,cx,cy,W,H):
        return min(2,int(cy/H*3))*3+min(2,int(cx/W*3))


