class PlayerTracker:
    def __init__(self):
        self.players:Dict[int,Player]={}; self._nid=1

    def update(self, dets, fps, mpp, clf, frame, W, H):
        unmatched=list(range(len(dets))); matched=set(); pairs=[]
        for di,(cx,cy,fy,xyxy,bbox) in enumerate(dets):
            for pid,p in self.players.items():
                d=math.hypot(cx-p.cx,cy-p.cy)
                if d<85: pairs.append((d,di,pid))
        pairs.sort()
        for d,di,pid in pairs:
            if di not in unmatched or pid in matched: continue
            cx,cy,fy,xyxy,bbox=dets[di]
            p=self.players[pid]
            pxm=math.hypot(cx-p.cx,cy-p.cy)
            spd=min(pxm*fps*mpp*3.6,42.0); dist=pxm*mpp
            p.cx,p.cy,p.feet_y=cx,cy,fy; p.bbox=bbox
            p.speed=round(spd,2); p.dist=round(p.dist+dist,2)
            hsv_col = clf.jersey_hsv(frame, xyxy)
            p.team  = clf.predict(hsv_col, cx, W)
            p.missing=0
            p.stats.tick(spd,dist)
            p.stats.positions.append((int(cx),int(cy)))
            p.stats.zone_counts[p.stats.zone_idx(cx,cy,W,H)]+=1
            matched.add(pid); unmatched.remove(di)
        for di in unmatched:
            cx,cy,fy,xyxy,bbox=dets[di]
            hsv_col = clf.jersey_hsv(frame, xyxy)
            t       = clf.predict(hsv_col, cx, W)
            p=Player(pid=self._nid,team=t,cx=cx,cy=cy,feet_y=fy,bbox=bbox)
            self.players[self._nid]=p; self._nid+=1
        for pid in list(self.players):
            if pid not in matched:
                self.players[pid].missing+=1
                if self.players[pid].missing>12:
                    del self.players[pid]
        return self.players


