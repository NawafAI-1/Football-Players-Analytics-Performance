def run():
    os.makedirs(DATASET_DIR, exist_ok=True)
    # ── ZIP extraction (always copy to fresh temp path, avoids OSError 22) ──
    if DATASET_ZIP and os.path.exists(DATASET_ZIP):
        try:
            import shutil, tempfile
            # Write to a brand-new temp file — avoids seek issues on files
            # with spaces or special chars in their names
            tmp_zip = os.path.join(tempfile.gettempdir(), "sap_dataset.zip")
            shutil.copy2(DATASET_ZIP, tmp_zip)
            with open(tmp_zip, "rb") as _fh:
                raw = _fh.read()
            import io
            with zipfile.ZipFile(io.BytesIO(raw), "r") as z:
                z.extractall(DATASET_DIR)
            os.remove(tmp_zip)
            print("[OK]  Dataset extracted to", DATASET_DIR)
        except Exception as _e:
            print("[WARN] ZIP extract failed:", _e)
            print("[WARN] Skipping — will use pretrained yolov8x.pt")
    else:
        print("[INFO] No dataset ZIP — will use pretrained yolov8x.pt")
    MODEL_PATH="/content/football_model/train/weights/best.pt"
    if os.path.exists(MODEL_PATH):
        model=YOLO(MODEL_PATH); print("[OK]  Custom model:",MODEL_PATH)
    else:
        model=YOLO("yolov8x.pt"); print("[INFO] Using yolov8x pretrained")
    print("Classes:",model.names)
    if not os.path.exists(VIDEO_IN):
        print(f"[ERROR] Video not found: {VIDEO_IN}"); return
    print("\n[INFO] Phase 1 — learning jersey colors…")
    clf=JerseyClassifier(); learn_colors(VIDEO_IN,clf,model)
    cap=cv2.VideoCapture(VIDEO_IN)
    fps=cap.get(cv2.CAP_PROP_FPS) or 30.0
    W=int(cap.get(cv2.CAP_PROP_FRAME_WIDTH)); H=int(cap.get(cv2.CAP_PROP_FRAME_HEIGHT))
    TOTAL=int(cap.get(cv2.CAP_PROP_FRAME_COUNT))
    mpp=PITCH_W_M/W; OUT_W=W+HUD_WIDTH
    writer=cv2.VideoWriter(VIDEO_OUT,cv2.VideoWriter_fourcc(*"mp4v"),fps,(OUT_W,H))
    tracker=PlayerTracker(); ptracker=PassTracker()
    pitch_roi=PitchROI(update_interval=30)
    t0=time.time(); fn=0
    all_players:Dict[int,Player]={}
    print(f"[INFO] Phase 2 — {W}x{H}  {fps:.1f}fps  {TOTAL} frames")
    print(f"[INFO] Output → {VIDEO_OUT}  ({OUT_W}x{H})\n")
    while True:
        ret,frame=cap.read()
        if not ret or (MAX_FRAMES and fn>=MAX_FRAMES): break
        roi=pitch_roi.update(frame,fn); rx1,ry1,rx2,ry2=roi
        res=model(frame,classes=[PERSON_CLS,BALL_CLS],conf=CONF_THRESH,verbose=False)[0]
        dets=[]; ball_xy=None
        for box in res.boxes:
            cls=int(box.cls[0]); xyxy=box.xyxy[0].cpu().numpy()
            x1,y1,x2,y2=map(int,xyxy)
            x1,y1=max(0,x1),max(0,y1); x2,y2=min(W,x2),min(H,y2)
            cx,cy=(x1+x2)/2.0,(y1+y2)/2.0
            if cls==BALL_CLS:
                if rx1<=cx<=rx2 and ry1<=cy<=ry2: ball_xy=(int(cx),int(cy))
            elif cls==PERSON_CLS:
                if not (rx1<=cx<=rx2 and ry1<=cy<=ry2): continue
                if y2>ry2: continue
                bh,bw=y2-y1,x2-x1
                if bh<H*MIN_BOX_H_FRAC: continue
                if bw>H*MAX_BOX_W_FRAC: continue
                if bh*bw<MIN_BOX_AREA: continue
                if bw>bh*1.1: continue
                if float(box.conf[0])<0.45: continue
                dets.append((cx,cy,float(y2),xyxy,(x1,y1,x2,y2)))
        players=tracker.update(dets,fps,mpp,clf,frame,W,H)
        holder_pid,_=ptracker.update(ball_xy,players,mpp,fn)
        for pid,p in players.items(): all_players[pid]=p
        for pid,p in players.items(): draw_player(frame,p,pid==holder_pid)
        if ball_xy: cv2.circle(frame,ball_xy,7,YELLOW,2,cv2.LINE_AA)
        draw_pitch_roi(frame,roi,show=False)
        draw_timeline(frame,ptracker.poss_log)
        draw_poss_panel(frame,ptracker)
        hud=np.zeros((H,HUD_WIDTH,3),np.uint8)
        draw_hud(hud,ptracker,players,fn,fps)
        writer.write(np.hstack([frame,hud]))
        fn+=1
        if fn%60==0:
            el=time.time()-t0
            na=sum(1 for p in players.values() if p.team==0)
            nb=sum(1 for p in players.values() if p.team==1)
            print(f"  {fn:5d}/{TOTAL}  {fn/TOTAL*100 if TOTAL else 0:.1f}%  {fn/el:.1f}fps  T1:{na} T2:{nb}  {ptracker.debug_str()}",end="\r")
    cap.release(); writer.release()
    dur=fn/fps
    print(f"\n\n[OK]  Video → {VIDEO_OUT}  ({os.path.getsize(VIDEO_OUT)/1e6:.1f} MB)")
    MAX_PER_TEAM=11
    def deduplicate(all_pl,max_per_team=MAX_PER_TEAM):
        result={}
        for tid in(0,1):
            team_players=sorted([p for p in all_pl.values() if p.team==tid],key=lambda p:p.stats.total_dist,reverse=True)
            for p in team_players[:max_per_team]: result[p.pid]=p
        return result
    final_players=deduplicate(all_players)
    print(f"[INFO] Dedup: {len(all_players)} IDs → {len(final_players)} players (T1:{sum(1 for p in final_players.values() if p.team==0)} T2:{sum(1 for p in final_players.values() if p.team==1)})")
    print("\n[INFO] Post-match assets…")
    make_heatmap(final_players,0,W,H,HEAT0); make_heatmap(final_players,1,W,H,HEAT1)
    make_passnet(final_players,ptracker.events,0,W,H,PNET0); make_passnet(final_players,ptracker.events,1,W,H,PNET1)
    export_csvs(final_players,ptracker)
    export_html(final_players,ptracker,dur,os.path.basename(VIDEO_IN))
    pct2=ptracker.pct()
    print("\n"+"═"*54); print("  FINAL STATISTICS"); print("═"*54)
    print(f"  Duration : {int(dur//60):02d}:{int(dur%60):02d}")
    for tid in(0,1):
        ps=[p for p in final_players.values() if p.team==tid]
        print(f"\n  {TEAM_NAMES[tid]}")
        print(f"    Ball Control : {pct2[tid]:.2f}%")
        print(f"    Passes       : {ptracker.passes[tid]}")
        print(f"    Players      : {len(ps)}")
        print(f"    Top speed    : {round(max((p.stats.top_speed for p in ps),default=0),2)} km/h")
        print(f"    Total dist   : {round(sum(p.stats.total_dist for p in ps),1)} m")
    print("═"*54); print(f"  Outputs → {OUT_DIR}/")


