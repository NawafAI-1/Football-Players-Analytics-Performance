#  JERSEY CLASSIFIER  — KMeans on HSV hue
# ════════════════════════════════════════════════
class JerseyClassifier:
    def __init__(self):
        self.fitted  = False
        self.centers = None
        self.km      = None

    def jersey_hsv(self, frame: np.ndarray, bbox) -> Optional[np.ndarray]:
        x1, y1, x2, y2 = map(int, bbox)
        jy1 = y1 + max(1, int((y2-y1)*0.10))
        jy2 = y1 + max(2, int((y2-y1)*0.45))
        crop = frame[max(0,jy1):jy2, max(0,x1):x2]
        if crop.size == 0 or crop.shape[0] < 3 or crop.shape[1] < 3:
            return None
        sm   = cv2.resize(crop, (20, 20))
        hsv  = cv2.cvtColor(sm, cv2.COLOR_BGR2HSV)
        msk  = ((hsv[:,:,2] > 50) & (hsv[:,:,2] < 230) & (hsv[:,:,1] > 30))
        flat = hsv.reshape(-1, 3).astype(float)
        if msk.sum() < 10:
            return None
        good = flat[msk.flatten()]
        return good.mean(axis=0)

    def jersey_color(self, frame: np.ndarray, bbox) -> np.ndarray:
        x1, y1, x2, y2 = map(int, bbox)
        jy1 = y1 + max(1, int((y2-y1)*0.10))
        jy2 = y1 + max(2, int((y2-y1)*0.45))
        crop = frame[max(0,jy1):jy2, max(0,x1):x2]
        if crop.size == 0:
            return np.array([128, 128, 128.0])
        sm   = cv2.resize(crop, (16, 16))
        hsv  = cv2.cvtColor(sm, cv2.COLOR_BGR2HSV)
        msk  = ((hsv[:,:,2] > 50) & (hsv[:,:,2] < 230) & (hsv[:,:,1] > 30))
        px   = sm.reshape(-1, 3).astype(float)
        if msk.sum() > 8:
            px = px[msk.flatten()]
        return px.mean(axis=0)

    def fit(self, hsv_samples: list, bgr_samples: list):
        if len(hsv_samples) < 20:
            print(f"[WARN] Only {len(hsv_samples)} samples — classifier unreliable")
            return
        arr = np.array(hsv_samples)
        hs  = arr[:, :2].copy()
        hs[:, 0] /= 180.0
        hs[:, 1] /= 255.0
        from sklearn.cluster import KMeans
        km = KMeans(n_clusters=2, random_state=42, n_init=20, max_iter=500)
        km.fit(hs)
        self.km = km
        labels  = km.labels_
        bgr_arr = np.array(bgr_samples)
        self.centers = np.array([
            bgr_arr[labels == 0].mean(axis=0),
            bgr_arr[labels == 1].mean(axis=0),
        ])
        self.fitted = True
        self.clus_to_team = {0: 0, 1: 1}
        sz0 = (labels == 0).sum()
        sz1 = (labels == 1).sum()
        print(f"  KMeans fitted: cluster0={sz0}  BGR≈{self.centers[0].astype(int)}")
        print(f"                 cluster1={sz1}  BGR≈{self.centers[1].astype(int)}")

    def predict(self, hsv_color: Optional[np.ndarray], cx: float, W: int) -> int:
        if not self.fitted or self.km is None or hsv_color is None:
            return 0 if cx < W * 0.5 else 1
        hs  = np.array([[hsv_color[0]/180.0, hsv_color[1]/255.0]])
        raw = int(self.km.predict(hs)[0])
        return self.clus_to_team.get(raw, raw)

    def update_color_memory(self, color: np.ndarray, team: int):
        pass  # no longer needed


