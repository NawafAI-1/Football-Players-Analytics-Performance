class PassTracker:
    def __init__(self):
        self.raw={0:0,1:0,-1:0}; self.passes={0:0,1:0}
        self.events:List[PassEvent]=[]
        self.history=deque(maxlen=POSS_SMOOTH_WIN)
        self.holder_pid=-1; self.holder_team=-1
        self.hold_frames=0
        self.holder_recv_x=0.0; self.holder_recv_y=0.0
        self.cooldown=0; self.curr_team=-1
        self.last_ball=None; self.ball_lost_fr=0
        self.attempted=0; self.rej_hold=0
        self.rej_dist=0; self.rej_inter=0
        self.poss_log:List[int]=[]

    def update(self, ball_xy, players:Dict, mpp:float, fn:int):
        if self.cooldown>0: self.cooldown-=1
        if ball_xy is not None:
            self.last_ball=ball_xy; self.ball_lost_fr=0
        elif self.last_ball is not None and self.ball_lost_fr<20:
            ball_xy=self.last_ball; self.ball_lost_fr+=1
        else:
            ball_xy=None
        holder_pid=-1
        if ball_xy and players:
            bx,by=ball_xy
            best_pid,best_d=-1,float("inf")
            for pid,p in players.items():
                d=math.hypot(p.cx-bx,p.feet_y-by)
                if d<best_d: best_d,best_pid=d,pid
            if best_d<POSS_RADIUS and best_pid!=-1:
                holder_pid=best_pid
                ht=players[best_pid].team
                self.raw[ht]+=1
                players[best_pid].stats.poss_frames+=1
                is_new_holder=(self.holder_pid!=-1 and self.holder_pid!=holder_pid)
                if is_new_holder and self.cooldown==0:
                    self.attempted+=1
                    prev=players.get(self.holder_pid)
                    if not prev:
                        pass
                    elif self.hold_frames<MIN_HOLD_FRAMES:
                        self.rej_hold+=1
                    else:
                        passer_team=prev.team
                        recv_pid=-1; recv_d=float("inf")
                        for pid2,p2 in players.items():
                            if pid2==self.holder_pid: continue
                            if p2.team!=passer_team: continue
                            d2=math.hypot(p2.cx-bx,p2.feet_y-by)
                            if d2<recv_d: recv_d=d2; recv_pid=pid2
                        if recv_pid==-1 or recv_d>POSS_RADIUS*2.5:
                            self.rej_inter+=1
                        else:
                            curr=players.get(recv_pid)
                            if curr:
                                dx=curr.cx-self.holder_recv_x
                                dy=curr.feet_y-self.holder_recv_y
                                dm=math.hypot(dx,dy)*mpp
                                if dm<MIN_PASS_M or dm>MAX_PASS_M:
                                    self.rej_dist+=1
                                else:
                                    t=passer_team
                                    self.passes[t]+=1
                                    self.cooldown=PASS_COOLDOWN
                                    prev.stats.passes_made+=1
                                    curr.stats.passes_recv+=1
                                    self.events.append(PassEvent(
                                        frame=fn,from_pid=self.holder_pid,
                                        to_pid=recv_pid,team=t,dist_m=round(dm,2),
                                        from_xy=(int(self.holder_recv_x),int(self.holder_recv_y)),
                                        to_xy=(int(curr.cx),int(curr.feet_y))))
                if holder_pid==self.holder_pid:
                    self.hold_frames+=1
                else:
                    cp=players.get(holder_pid)
                    self.hold_frames=1
                    self.holder_recv_x=cp.cx if cp else bx
                    self.holder_recv_y=cp.feet_y if cp else by
                self.holder_pid=holder_pid; self.holder_team=ht
            else:
                self.raw[-1]+=1
                if self.ball_lost_fr>6: self.holder_pid=-1
        else:
            self.raw[-1]+=1; self.holder_pid=-1
        entry=players[holder_pid].team if holder_pid in players else -1
        self.history.append(entry); self.poss_log.append(entry)
        cnt={0:0,1:0,-1:0}
        for t in self.history: cnt[t]+=1
        self.curr_team=max(cnt,key=cnt.get)
        return holder_pid,self.curr_team

    def pct(self):
        total=max(1,self.raw[0]+self.raw[1])
        return {0:round(100*self.raw[0]/total,2),1:round(100*self.raw[1]/total,2)}

    def debug_str(self):
        return (f"T1:{self.passes[0]} T2:{self.passes[1]}  "
                f"[att:{self.attempted} ok:{len(self.events)} "
                f"rh:{self.rej_hold} rd:{self.rej_dist} ri:{self.rej_inter}]")


